import { marked } from 'marked'
import matter from 'gray-matter'
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const contentDir = path.join(__dirname, '../content/articles')
const outputDir = path.join(__dirname, '../src/data')

// 配置 marked
marked.setOptions({
  breaks: true,
  gfm: true,
})

async function buildArticles() {
  const articles = []
  
  // 读取所有 markdown 文件
  const files = fs.readdirSync(contentDir).filter(f => f.endsWith('.md'))
  
  console.log(`\n📝 Processing ${files.length} articles...\n`)
  
  for (const file of files) {
    const filePath = path.join(contentDir, file)
    const content = fs.readFileSync(filePath, 'utf-8')
    
    // 解析 frontmatter
    const { data, content: markdown } = matter(content)
    
    // 生成 slug
    const slug = file.replace('.md', '')
    
    console.log(`Processing: ${slug}`)
    
    // 直接使用原始 markdown（包含图片链接）
    // 图片保持原始 URL，不下载
    
    // 转换 markdown 为 HTML
    const html = await marked(markdown)
    
    articles.push({
      slug,
      title: data.title,
      author: data.author,
      date: data.date,
      category: data.category,
      originalUrl: data.originalUrl,
      readTime: data.readTime || '10 分钟阅读',
      excerpt: generateExcerpt(markdown),
      content: html,
    })
  }
  
  // 按日期排序（最新的在前）
  articles.sort((a, b) => new Date(b.date) - new Date(a.date))
  
  // 生成 TypeScript 文件
  const output = `// Auto-generated by build-articles.js
// Do not edit this file directly. Add articles to content/articles/

const articles = ${JSON.stringify(articles, null, 2)}

export default articles
`
  
  fs.writeFileSync(path.join(outputDir, 'articles.ts'), output)
  console.log(`\n✅ Built ${articles.length} articles\n`)
}

function generateExcerpt(markdown) {
  // 获取第一段作为摘要
  const lines = markdown.split('\n')
  const paragraphs = []
  let currentPara = ''
  
  for (const line of lines) {
    if (line.trim() === '') {
      if (currentPara.trim()) {
        paragraphs.push(currentPara.trim())
      }
      currentPara = ''
    } else if (!line.startsWith('#') && !line.startsWith('---') && !line.startsWith('![')) {
      currentPara += ' ' + line
    }
  }
  
  // 取前两个段落
  const excerpt = paragraphs.slice(0, 2).join(' ').substring(0, 200)
  return excerpt.trim() + (excerpt.length >= 200 ? '...' : '')
}

buildArticles().catch(console.error)
